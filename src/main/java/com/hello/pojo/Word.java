package com.hello.pojo;

public class Word {
    private int word_id;
    private String word_name;
    private String word_meaning;
    private String word_buchong;

    public int getWord_id() {
        return word_id;
    }

    public void setWord_id(int word_id) {
        this.word_id = word_id;
    }

    public String getWord_name() {
        return word_name;
    }

    public void setWord_name(String word_name) {
        this.word_name = word_name;
    }

    public String getWord_meaning() {
        return word_meaning;
    }

    public void setWord_meaning(String word_meaning) {
        this.word_meaning = word_meaning;
    }

    public String getWord_buchong() {
        return word_buchong;
    }

    public void setWord_buchong(String word_buchong) {
        this.word_buchong = word_buchong;
    }
}
